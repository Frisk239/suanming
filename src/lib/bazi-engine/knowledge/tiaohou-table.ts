/** 天干 → 五行（用于把调候用神的天干转五行） */
const GAN_TO_WX: Record<string, string> = {
  甲: '木', 乙: '木', 丙: '火', 丁: '火', 戊: '土',
  己: '土', 庚: '金', 辛: '金', 壬: '水', 癸: '水',
};

/**
 * 取调候用神五行。
 * suangua 描述串里"首个出现的十天干字"为主用神
 * （如"丙为主，癸为佐"→ 丙 → 火）。
 */
export function getTiaohouWuxing(dayGan: string, monthZhi: string): string | null {
  const desc = TIAO_HOU_TABLE[dayGan]?.[monthZhi];
  if (!desc) return null;
  for (const ch of desc) {
    if (GAN_TO_WX[ch]) return GAN_TO_WX[ch];
  }
  return null;
}
